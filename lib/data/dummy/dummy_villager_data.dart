  }

  /// Get villager by NIK
  static Villager? getVillagerByNik(String nik) {
    try {
      return allVillagers.firstWhere((v) => v.nik == nik);
    } catch (e) {
      return null;
    }
  }

  /// Get villagers as JSON by village ID
